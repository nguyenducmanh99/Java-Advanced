package com.vti.filter;

public class ListfriendFilter {
	private int minID;

	private int maxID;

	private int minage;

	public ListfriendFilter() {
	}

	public int getMinID() {
		return minID;
	}

	public void setMinID(int minID) {
		this.minID = minID;
	}

	public int getMaxID() {
		return maxID;
	}

	public void setMaxID(int maxID) {
		this.maxID = maxID;
	}

	public int getMinAge() {
		return minage;
	}

	public void setMinAge(int minAge) {
		this.minage = minAge;
	}
}
